    #include<stdio.h>

int main(){
    //increment operator 
    // ++i preincrement i++ postincrement

    int i=1;
    printf("%d\n", i++); // i++ means; use,then increase 
    printf("%d\n", i);

    printf("%d\n", ++i); // increase, then use 
    printf("%d\n", i);



// same with --i and i--
    return 0;

}